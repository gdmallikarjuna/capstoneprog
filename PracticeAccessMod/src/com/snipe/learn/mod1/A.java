package com.snipe.learn.mod1;

public class A {
  int def_a;
  private int pri_a;
  protected int pro_a;
  public int pub_a;
  
  public void display() {
	  System.out.println("default modifier:;"+this.def_a);
	  System.out.println("private modifier:;"+this.pri_a);
	  System.out.println("protected modifier:;"+this.pro_a);
	  System.out.println("public modifier:;"+this.pub_a);
	  
  }
  
  
  
}
